/** induty_code → 한글 업종명. 매핑 없으면 원본 코드 반환. */
export function industryName(indutyCode: string): string {
  const c = (indutyCode || '').trim();
  if (!c) return '';
  const name = KSIC2[c.slice(0, 2)];
  return name || c; // 미등록 코드는 코드 그대로
}
